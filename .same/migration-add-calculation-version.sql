-- ==========================================
-- МИГРАЦИЯ: Добавление версионирования расчётов
-- ==========================================
-- Дата: 08 февраля 2026
-- Цель: Поддержка двух версий логики расчётов (v1 - старая, v2 - 4 кассы)
--
-- v1 (СТАРАЯ ЛОГИКА):
--   - Расходы Жигуля/Клычи распределяются:
--     * 50% вычитается из их ЗП
--     * 25% идёт на команду Вохи
--     * 25% идёт на команду Зета
--
-- v2 (НОВАЯ ЛОГИКА - 4 независимые кассы):
--   - Расходы Жигуля/Клычи распределяются:
--     * 50% вычитается из их ЗП
--     * 50% идёт в офисные расходы (НЕ на команды)
--   - Команды Вохи и Зета НЕ платят за расходы Жигуля/Клычи
--   - 4 независимые кассы: Воха, Зет, Жигуль, Клыча
-- ==========================================

-- ШАГ 1: Добавить колонку calculation_version
ALTER TABLE finance_periods
ADD COLUMN calculation_version TEXT DEFAULT 'v1';

-- ШАГ 2: Добавить комментарий
COMMENT ON COLUMN finance_periods.calculation_version IS
'Версия логики расчётов: v1 (старая - расходы распределяются на команды), v2 (новая - 4 независимые кассы)';

-- ШАГ 3: Пометить закрытый период как v1 (СОХРАНИТЬ СТАРУЮ ЛОГИКУ)
UPDATE finance_periods
SET calculation_version = 'v1'
WHERE id = 'period-2026-01-26';

-- ШАГ 4: Пометить второй период (уже созданный) как v2 (НОВАЯ ЛОГИКА)
UPDATE finance_periods
SET calculation_version = 'v2'
WHERE id = 'period-2026-02-09';

-- ШАГ 5: Все будущие периоды будут создаваться с v2 автоматически (см. FinanceContext.tsx)

-- ШАГ 6: Добавить проверочный constraint (опционально)
ALTER TABLE finance_periods
ADD CONSTRAINT check_calculation_version
CHECK (calculation_version IN ('v1', 'v2'));

